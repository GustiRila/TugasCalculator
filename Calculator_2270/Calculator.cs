using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator_2270
{
    class Calculator
    {
        public float tambah(float NilaiA,float NilaiB)
        {
            return NilaiA + NilaiB;
        }
        public float kurang(float NilaiA, float NilaiB)
        {
            return NilaiA - NilaiB;
        }
        public float kali(float NilaiA, float NilaiB)
        {
            return NilaiA * NilaiB;
        }
        public float bagi(float NilaiA, float NilaiB)
        {
            return NilaiA / NilaiB;
        }

    }
}
